# 2) Relational operator?
# - Compare two values and return true or false.

x = 5 
y = 10
print("------Relational Operator-------")
print("Equal to:",x == y)
print("Not Equal:",x!=y)
print("Greater-than:",x>y)
print("Less-than:",x<y)
print("Greater or Equal:",x>=y)
print("Less or Equal:",x<=y)